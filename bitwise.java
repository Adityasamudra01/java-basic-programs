public class bitwise {
    public static void main(String args[]){
        int a = 60;
        int b = 20;
        System.out.println("a & b   = "+ (a & b));
        System.out.println("a | b   = "+ (a | b));
        System.out.println("a <<  2 = "+ (a <<  2));
        System.out.println("a >>  2 = "+ (a >>  2));
        System.out.println("a >>> 2 = "+ (a >>> 2));
    }
}
